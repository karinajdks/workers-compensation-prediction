import streamlit as st

def presentation_page():
    st.title("🎓 Презентация проекта")

    # Используем вкладки или expander вместо reveal_slides
    tab1, tab2, tab3, tab4, tab5, tab6, tab7 = st.tabs([
        "📌 Введение", 
        "📁 Данные", 
        "🛠️ Предобработка", 
        "🤖 Модели", 
        "📊 Результаты", 
        "💻 Приложение", 
        "✅ Заключение"
    ])

    with tab1:
        st.header("Введение")
        st.markdown("""
        - Анализ данных о страховых случаях компенсации работникам
        - Цель: предсказать итоговую стоимость страхового возмещения
        - Датасет: Workers Compensation (100,000 записей)
        
        ### Бизнес-задача
        - Страховые компании нуждаются в точной оценке будущих выплат
        - Начальная оценка часто отличается от итоговой стоимости
        - Точные прогнозы помогают формировать резервы и тарифы
        """)

    with tab2:
        st.header("Данные")
        st.markdown("""
        **Источник:** OpenML (ID: 42876)
        
        **Признаки:**
        - Демографические: Age, Gender, MaritalStatus
        - Экономические: WeeklyPay
        - Временные: DateTimeOfAccident, DateReported
        - Медицинские: ClaimDescription
        - Оценочные: InitialCaseEstimate
        
        **Целевая переменная:** UltimateIncurredClaimCost (итоговая стоимость выплат)
        
        Объём данных: 100,000 записей
        """)

    with tab3:
        st.header("Предобработка данных")
        st.markdown("""
        **Выполненные шаги:**
        
        1. **Работа с датами**
           - Извлечение месяца и дня недели
           - Расчёт задержки отчетности (ReportingDelay)
        
        2. **Кодирование категорий**
           - LabelEncoder для Gender, MaritalStatus, PartTimeFullTime, ClaimDescription
        
        3. **Масштабирование**
           - StandardScaler для числовых признаков
        
        4. **Логарифмическое преобразование**
           - np.log1p для целевой переменной (стабилизация дисперсии)
        """)

    with tab4:
        st.header("Модели машинного обучения")
        st.markdown("""
        **Обученные модели:**
        
        | Модель | Преимущества |
        |--------|--------------|
        | Linear Regression | Простота, интерпретируемость |
        | Ridge Regression | Борьба с переобучением |
        | Random Forest | Работа с нелинейностями |
        | XGBoost | Высокая точность |
        
        **Разделение данных:** 80% обучение / 20% тест
        """)

    with tab5:
        st.header("Результаты")
        st.markdown("""
        **Метрики качества:**
        
        - **MAE** (Mean Absolute Error) — средняя абсолютная ошибка
        - **RMSE** (Root Mean Squared Error) — корень из средней квадратичной ошибки
        - **R² Score** — коэффициент детерминации
        
        **Полученные результаты:**
        - R² ≈ 0.09 (лучшая модель: XGBoost)
        - MAE ≈ $15,000–17,000
        - RMSE ≈ $46,000–48,000
        
        **Анализ:** Низкий R² объясняется отсутствием в датасете ключевых факторов (тяжесть травмы, качество лечения, судебные издержки).
        """)

    with tab6:
        st.header("Streamlit-приложение")
        st.markdown("""
        **Функционал:**
        
        1. Загрузка данных с OpenML
        2. Автоматическая предобработка
        3. Обучение 4 моделей регрессии
        4. Сравнение метрик качества
        5. Визуализация важности признаков
        6. График предсказаний vs реальность
        7. Интерактивное предсказание для новых случаев
        
        **Технологии:** Streamlit, Scikit-learn, Pandas, Matplotlib, XGBoost
        """)
        
        # Скриншоты можно добавить сюда
        st.info("Скриншоты работы приложения добавлены в отчёт")

    with tab7:
        st.header("Заключение")
        st.markdown("""
        **Итоги работы:**
        
        - ✅ Разработана система прогнозирования страховых выплат
        - ✅ Реализована полная пайплайн обработки данных
        - ✅ Создан интерактивный веб-интерфейс
        
        **Возможные улучшения:**
        
        1. Добавление внешних данных (медицинские показатели)
        2. GridSearch для подбора гиперпараметров
        3. Ансамблирование моделей
        4. Использование CatBoost/LightGBM
        
        **Заключение:** Проект демонстрирует полный цикл разработки ML-решения — от загрузки данных до интерактивного предсказания.
        """)

    st.divider()
    st.caption("📊 Проект по регрессии для прогнозирования стоимости страховых выплат")

if __name__ == "__main__":
    presentation_page()
